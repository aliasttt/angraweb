// Navbar functionality
function initNavbar() {
    const navbar = document.querySelector('.navbar');
    const navLinks = document.querySelectorAll('.navbar-nav .nav-link');
    const navbarCollapse = document.querySelector('.navbar-collapse');
    
    // Navbar background on scroll
    window.addEventListener('scroll', function() {
        if (window.scrollY > 50) {
            navbar.style.background = 'rgba(26, 26, 46, 0.95)';
            navbar.style.boxShadow = '0 2px 20px rgba(116, 185, 255, 0.1)';
        } else {
            navbar.style.background = 'rgba(26, 26, 46, 0.9)';
            navbar.style.boxShadow = 'none';
        }
    });
    
    // Active nav link highlighting
    window.addEventListener('scroll', function() {
        let current = '';
        const sections = document.querySelectorAll('section');
        
        sections.forEach(section => {
            const sectionTop = section.offsetTop;
            const sectionHeight = section.clientHeight;
            if (window.scrollY >= (sectionTop - 200)) {
                current = section.getAttribute('id');
            }
        });
        
        navLinks.forEach(link => {
            link.classList.remove('active');
            if (link.getAttribute('href') === `#${current}`) {
                link.classList.add('active');
            }
        });
    });
    
    // Prevent menu from closing when clicking on dropdown toggle
    const dropdownToggle = document.querySelector('.dropdown-toggle');
    if (dropdownToggle) {
        dropdownToggle.addEventListener('click', function(e) {
            e.stopPropagation(); // Prevent event bubbling
            // Don't close the navbar collapse when clicking dropdown toggle
        });
    }
    
    // Mobile menu close on link click (but not on dropdown toggle)
    navLinks.forEach(link => {
        link.addEventListener('click', function(e) {
            // Don't close menu if clicking on dropdown toggle
            if (link.classList.contains('dropdown-toggle')) {
                e.stopPropagation(); // Prevent closing menu
                return; // Let Bootstrap handle dropdown toggle
            }
            
            // Only close if it's a regular nav link (not dropdown)
            if (navbarCollapse && navbarCollapse.classList.contains('show')) {
                const bsCollapse = new bootstrap.Collapse(navbarCollapse);
                bsCollapse.hide();
            }
        });
    });
    
    // Close menu when clicking on dropdown items (social media links)
    const dropdownItems = document.querySelectorAll('.dropdown-item');
    dropdownItems.forEach(item => {
        item.addEventListener('click', function() {
            if (navbarCollapse && navbarCollapse.classList.contains('show')) {
                // Small delay to allow link to open first
                setTimeout(() => {
                    const bsCollapse = new bootstrap.Collapse(navbarCollapse);
                    bsCollapse.hide();
                }, 100);
            }
        });
    });
    
    // Prevent navbar collapse from closing when clicking inside dropdown menu
    const dropdownMenu = document.querySelector('.dropdown-menu');
    if (dropdownMenu) {
        dropdownMenu.addEventListener('click', function(e) {
            e.stopPropagation(); // Prevent click from bubbling to navbar
        });
    }
}

// Smooth scrolling for navigation links - Android compatible
function initSmoothScrolling() {
    const links = document.querySelectorAll('a[href^="#"]');
    
    links.forEach(link => {
        link.addEventListener('click', function(e) {
            const targetId = this.getAttribute('href');
            const targetSection = document.querySelector(targetId);
            
            if (targetSection) {
                // Check if smooth scrolling is supported
                const supportsSmoothScroll = 'scrollBehavior' in document.documentElement.style;
                
                if (supportsSmoothScroll) {
                    e.preventDefault();
                const offsetTop = targetSection.offsetTop - 80; // Account for fixed navbar
                
                window.scrollTo({
                    top: offsetTop,
                    behavior: 'smooth'
                });
                } else {
                    // Fallback for browsers without smooth scroll support (including some Android browsers)
                    e.preventDefault();
                    const offsetTop = targetSection.offsetTop - 80;
                    
                    // Use requestAnimationFrame for smooth scrolling on Android
                    const start = window.pageYOffset;
                    const distance = offsetTop - start;
                    const duration = 500;
                    let startTime = null;
                    
                    function animation(currentTime) {
                        if (startTime === null) startTime = currentTime;
                        const timeElapsed = currentTime - startTime;
                        const run = easeInOutQuad(timeElapsed, start, distance, duration);
                        window.scrollTo(0, run);
                        if (timeElapsed < duration) requestAnimationFrame(animation);
                    }
                    
                    function easeInOutQuad(t, b, c, d) {
                        t /= d / 2;
                        if (t < 1) return c / 2 * t * t + b;
                        t--;
                        return -c / 2 * (t * (t - 2) - 1) + b;
                    }
                    
                    requestAnimationFrame(animation);
                }
                
                // Update URL hash for proper anchor behavior
                if (history.pushState) {
                    history.pushState(null, null, targetId);
                } else {
                    window.location.hash = targetId;
                }
            }
        });
    });
    
    // Handle direct anchor links (e.g., from Google Ads)
    if (window.location.hash) {
        const hash = window.location.hash;
        const targetSection = document.querySelector(hash);
        
        if (targetSection) {
            // Wait for page to fully load
            window.addEventListener('load', function() {
                setTimeout(function() {
                    const offsetTop = targetSection.offsetTop - 80;
                    window.scrollTo({
                        top: offsetTop,
                        behavior: 'smooth'
                    });
                }, 100);
            });
        }
    }
}

